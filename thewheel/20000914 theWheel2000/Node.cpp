/////////////////////////////////////////////////////////////////////////////
// CNode::GetLink
//
// Returns the link to the given node, or NULL if none
//
/////////////////////////////////////////////////////////////////////////////
//##ModelId=3985F3710220
CNodeLink * CNode::GetLink(CNode * toNode)
{
	map<CNode *, CNodeLink>::iterator iter = 
		m_mapLinks.find(toNode);

	if (iter != m_mapLinks.end())
	{
		return &iter->second;
	}
	else
	{
		return NULL;
	}
}


