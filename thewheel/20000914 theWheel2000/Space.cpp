/////////////////////////////////////////////////////////////////////////////
// CSpace::GetNodeCount
//
// Returns the number of nodes in the space
//
/////////////////////////////////////////////////////////////////////////////
//##ModelId=39A59F3A0216
int CSpace::GetNodeCount()
{
	return m_arrNodes.size();
}


/////////////////////////////////////////////////////////////////////////////
// CSpace::GetNode
//
// Returns a node by index
//
/////////////////////////////////////////////////////////////////////////////
//##ModelId=39A59F3A020F
CNode * CSpace::GetNode(int nIndex)
{
	return m_arrNodes[nIndex];
}


/////////////////////////////////////////////////////////////////////////////
// CSpace::AddNode
//
// Adds a new node to the space.  Does no inter-linking of the node
//
/////////////////////////////////////////////////////////////////////////////
//##ModelId=39A59F3A020D
int CSpace::AddNode(CNode *pNewNode)
{
	m_arrNodes.push_back(pNewNode);
	return m_arrNodes.size()-1;
}


