/////////////////////////////////////////////////////////////////////////////
// Class CNodeLink Implementation
//
// A class that represent the link from its parent node to another node
//
/////////////////////////////////////////////////////////////////////////////


/////////////////////////////////////////////////////////////////////////////
// CNodeLink::CNodeLink
//
// Constructs a new node link to the given target node, with the given 
// weight.
//
/////////////////////////////////////////////////////////////////////////////
//##ModelId=39B6C8D202DE
CNodeLink::	CNodeLink(CNode *pToNode, float weight)
	: m_pToNode(pToNode),
		m_weight(weight)
{
}


/////////////////////////////////////////////////////////////////////////////
// CNodeLink::GetWeight
//
// Returns the link's weight
//
/////////////////////////////////////////////////////////////////////////////
//##ModelId=3985F45E02FD
float CNodeLink::GetWeight()
{
	return m_weight;
}


/////////////////////////////////////////////////////////////////////////////
// CNodeLink::SetWeight
//
// Sets a new value for the link's weight
//
/////////////////////////////////////////////////////////////////////////////
//##ModelId=3985F46501D0
void CNodeLink::SetWeight(float weight)
{
	m_weight = weight;
}


/////////////////////////////////////////////////////////////////////////////
// CNodeLink::GetToNode
//
// Returns this link's target
//
/////////////////////////////////////////////////////////////////////////////
//##ModelId=3985F47D03B6
CNode *CNodeLink::GetToNode()
{
	return m_pToNode;
}
